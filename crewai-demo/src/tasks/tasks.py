"""
CrewAI Task 定义 —— 将软件开发全流程拆分为 5 个独立任务

每个 Task 明确指定：
- description: 详细任务描述（包含占位符 {topic} 和上下文引用）
- expected_output: 期望输出格式
- agent: 负责执行的 Agent
- context: 上游依赖任务（CrewAI 自动将上下文注入到 description 中）
"""
from crewai import Task


def create_requirement_task(agent, topic: str) -> Task:
    """
    任务 1：需求分析

    由产品经理负责，产出 PRD 文档。
    这是整个流程的起点，不依赖其他任务。
    """
    return Task(
        description=(
            f"## 用户原始需求\n{topic}\n\n"
            "## 任务要求\n"
            "1. **需求理解**：仔细分析用户需求，识别核心痛点和目标用户群\n"
            "2. **竞品分析**：简要分析市面上已有的类似产品/方案\n"
            "3. **功能拆解**：将需求拆解为具体功能点，标注优先级（P0/P1/P2）\n"
            "4. **边界定义**：明确哪些功能本次做、哪些不做、哪些延后\n"
            "5. **非功能需求**：梳理性能、安全、可用性等非功能指标\n"
            "6. **验收标准**：为每个 P0 功能定义可量化的验收标准\n\n"
            "## 工具使用\n"
            "请使用 requirement_analyzer 工具对需求进行结构化分析。\n\n"
            "## 输出格式\n"
            "请以产品需求文档（PRD）格式输出，包含以下章节：\n"
            "- 项目背景与目标\n"
            "- 目标用户画像\n"
            "- 竞品分析摘要\n"
            "- 功能需求列表（含优先级和验收标准）\n"
            "- 非功能需求\n"
            "- 版本规划（V1.0 范围 vs 后续迭代）"
        ),
        expected_output=(
            "一份完整的产品需求文档（PRD），包含结构化的功能列表、"
            "优先级标注、验收标准和非功能需求。输出长度不少于 500 字。"
        ),
        agent=agent,
        async_execution=False,
    )


def create_architecture_task(agent, topic: str) -> Task:
    """
    任务 2：技术方案设计

    由架构师负责，基于 PRD 设计系统架构。
    依赖：任务 1 的 PRD 输出
    """
    return Task(
        description=(
            f"## 项目背景\n{topic}\n\n"
            "## 任务要求\n"
            "基于产品需求文档（PRD），完成以下工作：\n"
            "1. **系统架构设计**：设计整体架构（单体 / 微服务 / 事件驱动），说明理由\n"
            "2. **技术栈选型**：选择前后端技术栈，使用 tech_stack_recommender 工具辅助选型\n"
            "3. **模块划分**：将系统拆分为功能模块，定义模块间的接口和依赖\n"
            "4. **数据模型设计**：设计核心数据实体和关系（ER 图文字描述）\n"
            "5. **API 设计**：列出核心 REST API 端点（方法、路径、请求/响应）\n"
            "6. **架构决策记录**：为每个关键技术决策记录 ADR（为什么这样选）\n\n"
            "## 工具使用\n"
            "- tech_stack_recommender：根据项目类型推荐技术栈\n"
            "- knowledge_search：检索架构设计相关知识\n\n"
            "## 输出格式\n"
            "请以技术方案文档格式输出，包含：\n"
            "- 架构总览（附图文字描述）\n"
            "- 技术栈选型与理由\n"
            "- 模块设计与职责\n"
            "- 核心数据模型\n"
            "- 核心 API 接口列表\n"
            "- 架构决策记录（不少于 3 条）"
        ),
        expected_output=(
            "一份完整的技术方案文档，包含系统架构设计、技术栈选型、"
            "模块划分、数据模型和 API 设计。输出长度不少于 800 字。"
        ),
        agent=agent,
        async_execution=False,
    )


def create_development_task(agent, topic: str) -> Task:
    """
    任务 3：代码实现

    由开发工程师负责，编写核心代码。
    依赖：任务 2 的技术方案
    """
    return Task(
        description=(
            f"## 项目背景\n{topic}\n\n"
            "## 任务要求\n"
            "根据技术方案文档，完成以下代码实现：\n"
            "1. **项目结构**：创建清晰的项目目录结构，体现分层架构\n"
            "2. **核心业务逻辑**：实现最核心的业务服务层代码（至少 2 个核心模块）\n"
            "3. **API 接口**：实现 RESTful API 路由和请求处理\n"
            "4. **数据访问层**：实现数据库模型和基本 CRUD 操作\n"
            "5. **错误处理**：统一的异常处理和错误响应格式\n"
            "6. **类型安全**：使用类型标注和验证\n\n"
            "## 代码规范\n"
            "- Python: 遵循 PEP 8，使用 typing\n"
            "- 每个函数/类必须有 docstring\n"
            "- 使用 logging 而非 print\n"
            "- 敏感信息使用环境变量\n\n"
            "## 自检\n"
            "编写完成后，使用 code_reviewer 工具对核心代码进行自审。\n\n"
            "## 输出格式\n"
            "请输出以下内容：\n"
            "- 项目目录结构说明\n"
            "- 核心代码文件及注释说明\n"
            "- API 接口文档（简要）\n"
            "- 代码自审结果"
        ),
        expected_output=(
            "完整的代码实现方案，包含项目结构、核心业务代码、"
            "API 接口实现和自审结果。输出长度不少于 1000 字。"
        ),
        agent=agent,
        async_execution=False,
    )


def create_testing_task(agent, topic: str) -> Task:
    """
    任务 4：质量测试

    由测试工程师负责，对实现进行测试验证。
    依赖：任务 3 的代码实现
    """
    return Task(
        description=(
            f"## 项目背景\n{topic}\n\n"
            "## 任务要求\n"
            "基于代码实现，完成全面的质量评估：\n"
            "1. **测试策略**：制定测试金字塔——单元测试、集成测试、E2E 测试的覆盖范围\n"
            "2. **测试用例设计**：为核心功能设计具体的测试用例（至少 5 个）\n"
            "3. **代码审查**：使用 code_reviewer 工具审查代码质量\n"
            "4. **性能评估**：分析可能的性能瓶颈和优化建议\n"
            "5. **安全评估**：检查常见安全漏洞（注入、XSS、CSRF 等）\n"
            "6. **风险评估**：给出风险矩阵和发布建议\n\n"
            "## 输出格式\n"
            "请以测试报告格式输出，包含：\n"
            "- 测试策略概述\n"
            "- 测试用例列表（含输入、预期输出、优先级）\n"
            "- 代码审查结果\n"
            "- 性能与安全评估\n"
            "- 缺陷汇总与风险评估\n"
            "- 发布建议（Go/No-Go）"
        ),
        expected_output=(
            "一份全面的测试报告，包含测试策略、测试用例、"
            "代码审查结果和风险评估。输出长度不少于 800 字。"
        ),
        agent=agent,
        async_execution=False,
    )


def create_deployment_task(agent, topic: str) -> Task:
    """
    任务 5：部署方案

    由运维工程师负责，完成 CI/CD 和部署方案。
    依赖：任务 4 的测试报告
    """
    return Task(
        description=(
            f"## 项目背景\n{topic}\n\n"
            "## 任务要求\n"
            "基于前面的所有产出，完成运维部署方案：\n"
            "1. **容器化方案**：编写 Dockerfile 和 docker-compose.yml 设计\n"
            "2. **CI/CD 流水线**：设计从代码提交到生产部署的完整流水线\n"
            "3. **部署架构**：设计部署拓扑（单机 / 集群 / K8s）\n"
            "4. **监控方案**：指标采集（Prometheus）、日志收集（ELK/Loki）、告警规则\n"
            "5. **运维手册**：常见操作（启动/停止/回滚/扩容/数据备份）的 SOP\n"
            "6. **部署检查**：使用 deploy_checker 工具验证部署就绪条件\n\n"
            "## 输出格式\n"
            "请以运维部署方案格式输出，包含：\n"
            "- 容器化配置说明\n"
            "- CI/CD 流水线描述\n"
            "- 部署拓扑图（文字描述）\n"
            "- 监控告警方案\n"
            "- 运维 SOP 手册\n"
            "- 部署检查清单"
        ),
        expected_output=(
            "一份可直接交付使用的运维部署方案，包含 Docker 配置、"
            "CI/CD 流水线、监控方案和运维 SOP。输出长度不少于 800 字。"
        ),
        agent=agent,
        async_execution=False,
    )
